'''
0부터 5까지의 값들을 다 더해야하는 조건임.
변수를 하나 만들어서 초깃값을 설정한다. 왜냐면 반복할때 쓰일 변수임.
한번 돌때 그 변수에 값이 들어가면 그 다음 반복때 활용할 수 있음 (업데이트 느낌)
그렇기 때문에 반복문에 변수를 쓴다.
0부터 5까지 반복해야하기 때문에 반복문 씀.
num의 값이 a로 0부터 대입됨(인덱싱).
현재 d 값과 현재 a의 값을 더하는 변수를 또 d로 쓰면 업데이트가 되어 누적되는 변수가 됨.
그렇게 num의 수에 따라 반복하게되면 업데이트 된 변수 d의 값이 최종 누적되어 다 더해진 값이 된다.
'''

num = [0, 1, 2, 3, 4, 5]

d = 0

for a in num:
    d = d + a

print(d) 




