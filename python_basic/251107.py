# range를 사용해서 9번 돌아가는 반복문을 사용함. (2, 10) 표시(종료 숫자는 -1이기 때문)
# 변수 a에 2부터 9까지 숫자가 대입됨.
# print로 변수 a와 문자 단을 표시함으로 몇단인지 알 수 있음.
for a in range(2, 10):
    print(a, '단')

    # 그다음 안에 1~9까지 곱셈 값을 보여줘야하기 때문에 반복문을 또 사용함.
    # range을 사용해서 1부터 9번 곱해야하니 (1, 10) 표시(종료 숫자는 -1이기 때문)
    
    for b in range(1, 10):
        print(a, 'x', b, '=', a*b)

    # 9번 돌아가야 이 반복문이 끝나고 2단이 완성됨.

# 이렇게 2단, 3단, 4단 ... 9단까지 다 반복되면 반복문 끝, 구구단 완성!




# '열번 찍어 안 넘어가는 나무 없다'를 파이썬 프로그램으로 코딩

# 열번을 찍어보아야 하기 때문에 반복문이 필요함.
# 그래서 변수를 만들어서 참이 될 때 반복 종료.

treeHit = 0

# 변수에 초깃값을 할당해서 10을 찍을 때까지 반복문을 돌린다.
# 만약 10이 된다면 '넘어갔다'라고 출력이 되고 반복문이 끝난다.

while treeHit < 10:
    treeHit = treeHit + 1
    print(treeHit, '찍었다')
    
    if treeHit == 10:
        print('넘어갔다')